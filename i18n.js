// ─── NÚCLEO DEL PLUGIN I18N ──────────────────────────────────────────────────

const i18n = {
    currentLang: 'es',

    /**
     * Inicializa el plugin. Lee el idioma guardado o usa 'es' por defecto.
     */
    init() {
        const saved = localStorage.getItem('md_lang') || 'es';
        this.setLang(saved, false);
        this._renderSwitcher();
    },

    /**
     * Cambia el idioma activo, actualiza el DOM y guarda la preferencia.
     * @param {string} lang - Código ISO: 'es', 'ca', 'en'
     * @param {boolean} animate - Si se reproduce la transición de fade
     */
    setLang(lang, animate = true) {
        if (!translations[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('md_lang', lang);

        const applyTranslations = () => {
            const t = translations[lang];
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (t[key] !== undefined) {
                    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                        el.setAttribute('placeholder', t[key]);
                    } else {
                        el.innerHTML = t[key];
                    }
                }
            });
            document.documentElement.setAttribute('lang', lang);
            this._updateSwitcherActive(lang);
        };

        if (animate) {
            document.body.style.opacity = '0.6';
            document.body.style.transition = 'opacity 0.25s ease';
            setTimeout(() => {
                applyTranslations();
                document.body.style.opacity = '1';
            }, 250);
        } else {
            applyTranslations();
        }
    },

    /**
     * Renderiza el selector flotante de idioma en el menú de navegación.
     */
    _renderSwitcher() {
        const switcher = document.getElementById('lang-switcher');
        if (!switcher) return;

        const flags = { es: '🇪🇸', ca: '🏳️', en: '🇬🇧' };
        const labels = { es: 'ES', ca: 'CA', en: 'EN' };

        Object.keys(translations).forEach(lang => {
            const btn = document.createElement('button');
            btn.className = 'lang-btn';
            btn.id = `lang-${lang}`;
            btn.setAttribute('aria-label', `Cambiar idioma a ${labels[lang]}`);
            btn.innerHTML = `${flags[lang]} ${labels[lang]}`;
            btn.addEventListener('click', () => this.setLang(lang));
            switcher.appendChild(btn);
        });

        this._updateSwitcherActive(this.currentLang);
    },

    _updateSwitcherActive(lang) {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.id === `lang-${lang}`);
        });
    }
};

// ─── INICIO ──────────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => i18n.init());
